package com.data.dto;

public class User {
	
	private String userID;
	private String userPassword;
	private String userName;
	private String userGender;
	private String userEmail;
	private String userPhoneNum;
	private String userDate;
	private int userEnabled;
	private String userSuspension;
	private int userAuthority;
	private String userJoinIP;
	private String userLastIP;

	
	public String getUserID() {
		return userID;
	}
	public void setUserID(String userID) {
		this.userID = userID;
	}
	public String getUserPassword() {
		return userPassword;
	}
	public void setUserPassword(String userPassword) {
		this.userPassword = userPassword;
	}
	public String getUserName() {
		return userName;
	}
	public void setUserName(String userName) {
		this.userName = userName;
	}
	public String getUserGender() {
		return userGender;
	}
	public void setUserGender(String userGender) {
		this.userGender = userGender;
	}
	public String getUserEmail() {
		return userEmail;
	}
	public void setUserEmail(String userEmail) {
		this.userEmail = userEmail;
	}
	public String getUserPhoneNum() {
		return userPhoneNum;
	}
	public void setUserPhoneNum(String userPhoneNum) {
		this.userPhoneNum = userPhoneNum;
	}
	public String getUserDate() {
		return userDate;
	}
	public void setUserDate(String userDate) {
		this.userDate = userDate;
	}
	public int getUserEnabled() {
		return userEnabled;
	}
	public void setUserEnabled(int userEnabled) {
		this.userEnabled = userEnabled;
	}
	public String getUserSuspension() {
		return userSuspension;
	}
	public void setUserSuspension(String userSuspension) {
		this.userSuspension = userSuspension;
	}
	public int getUserAuthority() {
		return userAuthority;
	}
	public void setUserAuthority(int userAuthority) {
		this.userAuthority = userAuthority;
	}
	public String getUserJoinIP() {
		return userJoinIP;
	}
	public void setUserJoinIP(String userJoinIP) {
		this.userJoinIP = userJoinIP;
	}
	public String getUserLastIP() {
		return userLastIP;
	}
	public void setUserLastIP(String userLastIP) {
		this.userLastIP = userLastIP;
	}
	
}
